package com.progra.segurosbackend.logic;

import java.util.Objects;

/**
 *
 * @author leoch
 */
public class Modelo {

    String nombre;
    int id;
    Marca marca;

    public Modelo() {
        this("", 0, new Marca());
    }

    public Modelo(String nombre, int id, Marca marca) {
        this.nombre = nombre;
        this.id = id;
        this.marca = marca;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Marca getMarca() {
        return marca;
    }

    public void setMarca(Marca marca) {
        this.marca = marca;
    }

    @Override
    public String toString() {
        return marca.getNombre() + "-" + nombre;
    }

    @Override
    public int hashCode() {
        int hash = 7;
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Modelo other = (Modelo) obj;
        if (this.id != other.id) {
            return false;
        }
        if (!Objects.equals(this.nombre, other.nombre)) {
            return false;
        }
        return Objects.equals(this.marca, other.marca);
    }

}
